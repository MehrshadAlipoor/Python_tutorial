# ======================== Error handling in Python ========================================
# what is an error?


# types of errors in Python:
#     # 1. Syntax errors:
#         # - Syntax errors occur when the code violates the syntax rules of Python.
#         # - These errors are detected by the Python interpreter before the code is executed.
#         # - Example:
#             # print("Hello, World!"  # Missing closing parenthesis

#      # 2. Runtime errors:
#         # - Runtime errors occur during the execution of the code.
#         # - These errors are not detected by the Python interpreter before execution.
#         # - Example:
#             # x = 10 / 0  # Division by zero error
#             # print(x)  # This line will not be executed due to the error above

#       #  3. Logical errors:
#         # - Logical errors occur when the code runs without any syntax or runtime errors, but produces incorrect results.
#         # - These errors are often caused by incorrect logic or assumptions in the code.
#         # - Example:
#             # def add_numbers(a, b):
#             #     return a - b  # Incorrect operation (should be addition)
#             # result = add_numbers(5, 3)
#             # print(result)  # Output will be incorrect (should be 8, but will be 2)
#         # - Logical errors are often the hardest to detect and fix, as they do not produce any error messages or exceptions.
#         # - Debugging techniques, such as print statements or using a debugger, can help identify and fix logical errors.
#         # - Example:
#             # def add_numbers(a, b):
#             #     return a + b  # Correct operation (addition)

